n1 = int(input("Digite seu primero numero: "))
n2 = int(input("Digite seu segundo numero: "))

if(n1 == n2 ):
    print("Seu numero e igual")
else:
    if(n1 < n2):
        print("Seu primeiro numero é menor que o segundo")
    else:
        print("Seu segundo numero  é menor que o primeiro")
